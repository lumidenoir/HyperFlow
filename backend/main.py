@app.delete("/session/{session_id}", tags=["Session"])
def delete_session(session_id: int, db: Session = Depends(get_session)):
    """Delete an entire session, its logs, and reverse its lingering fatigue."""
    session = db.get(WorkoutSession, session_id)
    if not session:
        raise HTTPException(status_code=404, detail="Session not found")

    # Fetch all logs for this session
    logs = db.exec(select(SetLog).where(SetLog.session_id == session_id)).all()

    # If the session was already completed, we MUST reverse the fatigue
    if session.status == "completed":
        session_fatigue = {}

        # 1. Re-calculate the raw fatigue this session originally generated
        for log in logs:
            ex = db.get(Exercise, log.exercise_id)
            if not ex:
                continue

            set_fatigue = calculate_set_fatigue(ex, log)
            session_fatigue[ex.primary_muscle] = (
                session_fatigue.get(ex.primary_muscle, 0) + set_fatigue
            )

            if ex.secondary_muscle:
                session_fatigue[ex.secondary_muscle] = session_fatigue.get(
                    ex.secondary_muscle, 0
                ) + (set_fatigue * 0.4)

        # 2. Calculate how much of that fatigue is STILL in the body today
        now = datetime.now()
        days_passed = (
            (now - session.date).total_seconds() / 86400.0 if session.date else 0
        )
        decay_factor = math.exp(-DECAY_K * max(0, days_passed))

        # 3. Subtract the lingering fatigue from the current muscle states
        for muscle, original_fatigue in session_fatigue.items():
            state = db.get(MuscleState, muscle)
            if state:
                lingering_fatigue = original_fatigue * decay_factor

                # Bring state up to 'now', then subtract the lingering amount
                current_decayed = get_decayed_fatigue(state)
                state.current_fatigue = max(0.0, current_decayed - lingering_fatigue)
                state.last_updated = now
                db.add(state)

    # 4. Delete the logs and the session itself
    for log in logs:
        db.delete(log)

    db.delete(session)
    db.commit()

    return {"message": "Session deleted and lingering fatigue reversed"}


