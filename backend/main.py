@app.get("/analytics/volume", tags=["Analytics"])
def get_volume_trends(
    muscle_group: Optional[str] = None,
    days: int = Query(30, ge=1, le=365),
    db: Session = Depends(get_session),
):
    """
    Get volume data for the last N days.
    Returns total volume per workout day, optionally filtered by muscle group.
    """
    from datetime import timedelta

    cutoff_date = datetime.now() - timedelta(days=days)

    # Get all recent sessions
    sessions = db.exec(
        select(WorkoutSession)
        .where(
            (WorkoutSession.status == "completed")
            & (WorkoutSession.date >= cutoff_date)
        )
        .order_by(WorkoutSession.date.asc())
    ).all()

    volume_data = {}
    for session in sessions:
        logs = db.exec(select(SetLog).where(SetLog.session_id == session.id)).all()

        for log in logs:
            ex = db.get(Exercise, log.exercise_id)
            if not ex:
                continue

            # Filter by muscle if specified
            if muscle_group:
                if muscle_group not in (ex.primary_muscle, ex.secondary_muscle):
                    continue

            date_key = session.date.strftime("%Y-%m-%d")
            if date_key not in volume_data:
                volume_data[date_key] = 0.0
            volume_data[date_key] += log.reps * log.weight

    return {
        "muscle_group": muscle_group or "all",
        "days": days,
        "data": volume_data,
    }


@app.get("/analytics/fatigue-trend", tags=["Analytics"])
def get_fatigue_recovery(
    muscle_group: str,
    db: Session = Depends(get_session),
):
    """
    Get current fatigue state and recovery estimate for a muscle group.
    Shows how long until below FATIGUE_LIMIT.
    """
    state = db.get(MuscleState, muscle_group)
    if not state:
        return {
            "muscle_group": muscle_group,
            "fatigue": 0.0,
            "recovered": True,
        }

    current_f = get_decayed_fatigue(state)

    # Calculate hours until below fatigue limit
    if current_f > FATIGUE_LIMIT:
        # Solve: current_f * e^(-k*t) = FATIGUE_LIMIT for t
        # t = ln(current_f / FATIGUE_LIMIT) / k
        recovery_days = math.log(current_f / FATIGUE_LIMIT) / DECAY_K
        recovery_hours = recovery_days * 24
    else:
        recovery_hours = 0

    return {
        "muscle_group": muscle_group,
        "fatigue": round(current_f, 2),
        "limit": FATIGUE_LIMIT,
        "recovered": current_f < FATIGUE_LIMIT,
        "estimated_recovery_hours": round(max(0, recovery_hours), 1),
    }


