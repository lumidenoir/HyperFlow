@app.post("/session/finish/{session_id}", tags=["Session"])
def finish_session(
    session_id: int,
    db: Session = Depends(get_session),
):
    """
    Finalize a session and update global muscle fatigue.
    This applies exponential decay to existing fatigue, then adds new fatigue from this session.
    """
    workout = db.get(WorkoutSession, session_id)
    if not workout:
        raise HTTPException(status_code=404, detail="Session not found")
    if workout.status != "active":
        raise HTTPException(status_code=400, detail="Session is already completed")

    # Replace your old logs query with this:
    from sqlmodel import select

    # Fetch logs and exercises together
    statement = (
        select(SetLog, Exercise)
        .join(Exercise, SetLog.exercise_id == Exercise.id)
        .where(SetLog.session_id == session_id)
    )
    results = db.exec(statement).all()

    if not results:
        workout.status = "completed"
        db.add(workout)
        db.commit()
        return {"message": "Empty session completed"}

    session_fatigue = {}
    total_volume = 0.0

    for log, ex in results:
        set_fatigue = calculate_set_fatigue(ex, log)

        session_fatigue[ex.primary_muscle] = (
            session_fatigue.get(ex.primary_muscle, 0) + set_fatigue
        )

        if ex.secondary_muscle:
            session_fatigue[ex.secondary_muscle] = session_fatigue.get(
                ex.secondary_muscle, 0
            ) + (set_fatigue * 0.4)

            total_volume += log.reps * log.weight

    # Update global muscle states with decay + new fatigue
    for muscle, added_fatigue in session_fatigue.items():
        state = db.get(MuscleState, muscle)
        if not state:
            state = MuscleState(muscle_group=muscle, current_fatigue=0.0)
            db.add(state)

        # Decay existing fatigue first, then add session fatigue
        decayed = get_decayed_fatigue(state)
        state.current_fatigue = decayed + added_fatigue
        state.last_updated = datetime.now()
        db.add(state)

    # Mark session complete and calculate duration
    workout.status = "completed"
    if workout.date:
        duration = (datetime.now() - workout.date).total_seconds() / 60
        workout.duration_minutes = max(1, int(duration))
    db.add(workout)
    db.commit()

    return {
        "message": "Workout saved and fatigue updated",
        "session_id": session_id,
        "fatigue_applied": session_fatigue,
        "total_volume": round(total_volume, 2),
    }


