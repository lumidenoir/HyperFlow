# --- Helper Functions ---


def get_decayed_fatigue(state: MuscleState) -> float:
    """Calculate current fatigue with exponential decay."""
    if not state:
        return 0.0
    now = datetime.now()
    days_passed = (now - state.last_updated).total_seconds() / 86400.0
    return max(0.0, state.current_fatigue * math.exp(-DECAY_K * days_passed))


def calculate_set_fatigue(exercise: Exercise, log: SetLog) -> float:
    """
    Calculate fatigue generated by a single set based on effort, not absolute tonnage.
    """
    # Base effort: If RPE isn't provided, assume a moderate-hard set (RPE 7)
    intensity_factor = (log.rpe / 10.0) if log.rpe else 0.7

    # Optional: Slightly scale up fatigue for higher rep ranges (metabolic stress)
    # e.g., 20 reps generates 20% more local fatigue than 5 reps
    rep_multiplier = 1.0 + (log.reps * 0.01)

    # Fatigue = Base Exercise Rating × Effort × Rep Modifier
    return exercise.fatigue_rating * intensity_factor * rep_multiplier


def get_muscle_status_with_decay(db: Session) -> dict:
    """Get all muscle groups with decayed fatigue values."""
    states = db.exec(select(MuscleState)).all()
    return {s.muscle_group: round(get_decayed_fatigue(s), 2) for s in states}


