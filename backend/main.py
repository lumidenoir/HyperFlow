@app.get("/status/{muscle_group}", tags=["Status"])
def get_muscle_status_single(muscle_group: str, db: Session = Depends(get_session)):
    """Get fatigue for a specific muscle group."""
    state = db.get(MuscleState, muscle_group)
    if not state:
        return {"muscle_group": muscle_group, "fatigue": 0.0, "status": "fresh"}

    decayed = get_decayed_fatigue(state)
    status = "red-zoned" if decayed >= FATIGUE_LIMIT else "available"

