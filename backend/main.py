@app.post("/session/generate", tags=["Planning"])
def generate_workout(
    priority_groups: List[str] = Query([]),
    db: Session = Depends(get_session),
):
    """
    Recommend exercises with dynamic set targets based on zoned fatigue.
    """
    if not priority_groups:
        return {
            "message": "No priority groups specified",
            "recommendations": [],
        }

    status = get_muscle_status_with_decay(db)
    recommendations = []

    for group in priority_groups:
        current_f = status.get(group, 0.0)

        # The Binary Cutoff Fix: Zoned Volume Calculation
        if current_f >= FATIGUE_LIMIT:
            # Zone: Fried (10.0+)
            recommendations.append(
                {
                    "warning": f"{group} is red-zoned ({current_f:.1f}/{FATIGUE_LIMIT}). Skip or do active recovery."
                }
            )
            continue

        elif current_f < 4.0:
            # Zone: Fresh (0.0 - 4.0)
            target_sets = 4
        elif current_f < 8.0:
            # Zone: Recovering (4.1 - 8.0)
            target_sets = 3
        else:
            # Zone: High Fatigue (8.1 - 9.9) - Tapering volume
            target_sets = 2

        # Fetch exercises where this group is primary
        exercises = db.exec(
            select(Exercise).where(Exercise.primary_muscle == group)
        ).all()

        # Attach the calculated target_sets to each exercise
        for ex in exercises:
            # .model_dump() converts the SQLModel to a standard dictionary
            ex_data = ex.model_dump()
            ex_data["target_sets"] = target_sets
            recommendations.append(ex_data)

    return {
        "priority_groups": priority_groups,
        "status": status,
        "recommendations": recommendations,
    }


@app.get("/workouts/history", tags=["History"])
def get_workout_history(
    db: Session = Depends(get_session),
    limit: int = Query(20, ge=1, le=100),
):
    """Get workout history (most recent first) with calculated stats."""
    sessions = db.exec(
        select(WorkoutSession)
        .where(WorkoutSession.status == "completed")
        .order_by(WorkoutSession.date.desc())
        .limit(limit)
    ).all()

    # Calculate the volume and sets for each session
    history_data = []
    for session in sessions:
        logs = db.exec(select(SetLog).where(SetLog.session_id == session.id)).all()
        total_volume = sum(log.reps * log.weight for log in logs)

        history_data.append(
            {
                "id": session.id,
                "date": session.date,
                "duration_minutes": session.duration_minutes,
                "total_sets": len(logs),
                "total_volume": round(total_volume, 2),
                "status": session.status,
                "planned_template": session.planned_template,
            }
        )

    return history_data


